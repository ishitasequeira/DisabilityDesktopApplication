    private void btnSetBudgetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetBudgetActionPerformed
        Network network = (Network) networkJComboBox.getSelectedItem();
        //String name = txtFieldBudget.getText();
        double budget = 0.0;
        double networkBudget = 0.0;
        if (txtFieldBudget.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a budget");
            java.util.logging.Logger.getLogger(NetworkBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Budget field is blank!");
            txtFieldBudget.setText("");
            return;
        }
        try {
            budget = Double.parseDouble(txtFieldBudget.getText());
            if (budget <= 0) {
                JOptionPane.showMessageDialog(null, "Value needs to be greater than 0");
                java.util.logging.Logger.getLogger(NetworkBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Budget needs  to be greater than 0!");
                return;
            }
            networkBudget = network.getBudget();
//            network.setBudget(budget + networkBudget);
//            txtFieldBudget.setText("");
//            JOptionPane.showMessageDialog(null, "Network Budget has been set.");

            Double.parseDouble(txtFieldBudget.getText());
        } catch (NumberFormatException e) {
            java.util.logging.Logger.getLogger(NetworkBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Budget needs to be numeric! Entered value: "+txtFieldBudget.getText());
            JOptionPane.showMessageDialog(this, "Please add numeric field for Budget");
            txtFieldBudget.setText("");
            return;
        }
        network.setBudget(budget + networkBudget);
        populateNetworkTable();
        JOptionPane.showMessageDialog(this, "Budget has been set.");
        java.util.logging.Logger.getLogger(NetworkBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Budget "+txtFieldBudget.getText()+" has been set successfully for Network "+network.getName());
        txtFieldBudget.setText("");

    }//GEN-LAST:event_btnSetBudgetActionPerformed

