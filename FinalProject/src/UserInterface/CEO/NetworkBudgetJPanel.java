    private void populateNetworkTable() {
        DefaultTableModel model = (DefaultTableModel) budgetJTable.getModel();
        model.setRowCount(0);
        //if(!organization.getEmployeeDirectory().getEmployeeList().contains(centerHead))
        for (Network network : disabilityCenter.getNetworkList()) {
            {
                Object[] row = new Object[2];
                row[0] = network.getName();
                row[1] = network.getBudget();
                model.addRow(row);
            }
        }
    }

