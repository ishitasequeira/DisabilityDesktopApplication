    private void btnCenterBudgetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCenterBudgetActionPerformed
        //double budg = 0.0;
        double budget = 0.0;
        double centerBudget = 0.0;
        Enterprise enterprise = (Enterprise) centerJCombo.getSelectedItem();
        try {
             budget = Double.parseDouble(txtCenterBudget.getText());
            if(budget <= 0){
                JOptionPane.showMessageDialog(null, "Value needs to be greater than 0");
                java.util.logging.Logger.getLogger(SetCenterBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Value needs to be greater than 0");
                return;
            }
             //budg = ((CenterEnterprise)enterprise).getCenterBudget();
             centerBudget = ((CenterEnterprise)enterprise).getCenterBudget();
            if (network.getBudget() - budget >= 0) {
                network.setBudget(network.getBudget() - budget);
                ((CenterEnterprise)enterprise).setCenterBudget(centerBudget+budget);
                txtCenterBudget.setText("");
                JOptionPane.showMessageDialog(null, "Center Budget has been set.");
                java.util.logging.Logger.getLogger(SetCenterBudgetJPanel.class.getName()).log(java.util.logging.Level.INFO, "Center Budget has been set to "+((CenterEnterprise)enterprise).getCenterBudget());
            } else {
                JOptionPane.showMessageDialog(this, "Please select budget less than Network Budget");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please add numeric field for Budget");
            txtCenterBudget.setText("");
            return;
        }
        lblBudget.setText(String.valueOf(network.getBudget()));
        //populateCenterBudgetTable(enterprise);
        
        populateTable();
        DB4OUtil.getInstance().storeSystem(disabilityCenter);
    }//GEN-LAST:event_btnCenterBudgetActionPerformed


