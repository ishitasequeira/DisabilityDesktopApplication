    public void populateCenterComboBox() {
        centerJCombo.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise instanceof CenterEnterprise) {
                centerJCombo.addItem(enterprise);
            }
        }
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCenterBudget.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise instanceof CenterEnterprise) {
                CenterEnterprise centerEnterprise = (CenterEnterprise) enterprise;
                Object[] row = new Object[model.getColumnCount()];
                row[1] = centerEnterprise.getCenterBudget();
                row[0] = enterprise;
                model.addRow(row);
            }
        }
    }

