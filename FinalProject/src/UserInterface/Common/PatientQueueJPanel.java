    public void refreshQueue() {
        DefaultTableModel model = (DefaultTableModel) patientQueueJTable.getModel();

        model.setRowCount(0);
        for (WorkRequest w : userAccount.getWorkQueue().getWorkRequestList()) {
            Patient p = (Patient) w.getObject();
            Object row[] = new Object[2];
            row[0] = p.getPatientID();
            row[1] = p;
            model.addRow(row);
        }
    }

