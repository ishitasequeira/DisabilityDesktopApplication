    private void btnDiagnosePatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiagnosePatientActionPerformed
        int row = patientQueueJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please make a selection!!");
            return;
        }
        Patient patient = (Patient)patientQueueJTable.getValueAt(row, 1);
        DiagnosePatientJPanel diagnosePatientJPanel = new DiagnosePatientJPanel(bottomPanel, userAccount, organization, patient, business, null);
        CardLayout cardLayout = (CardLayout) bottomPanel.getLayout();
        bottomPanel.add(diagnosePatientJPanel);
        cardLayout.next(bottomPanel);
    }//GEN-LAST:event_btnDiagnosePatientActionPerformed


